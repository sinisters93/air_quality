# Function to process the emission file and proxy file
def process_emission_data(emission_file_name, proxy_file_name, selected_species):
    
    
    # Load the emission data file
    emission_df = pd.read_excel(emission_file_name, skiprows=1)
    
    # Get the emission value for the selected species
    emission_value = emission_df[selected_species].iloc[0]
    print(f"The emission value for {selected_species} is: {emission_value}\n")
    print("Processing Further...Please Wait!\n")

    # Load the proxy data file
    proxy_df = pd.read_excel(proxy_file_name)

# Multiply the emission value with the proxy data for each latitude and longitude in each month
    for col in proxy_df.columns[2:]:
        proxy_df[col] *= emission_value
        month_total = proxy_df[col].sum()
        print(f"Processed month: {col}, Total: {month_total} Gg/yr")
    
    # Assuming your monthly columns are named from 'Jan' to 'Dec' and you have an 'Annual' column
    monthly_columns = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
    proxy_df['Annual'] = proxy_df[monthly_columns].sum(axis=1)  # Sum up monthly values for the annual column
    
    # Calculate the sum for each month and the annual sum
    monthly_sums = proxy_df[monthly_columns].sum()
    annual_sum = proxy_df['Annual'].sum()
    print(f"Processed Annual: Ann, Total: {annual_sum} Gg/yr\n")# Corrected variable name here
    print("Processing Further...Please Wait!\n")
    
    # Create a new row with sums for each month and the annual sum
    sums_row = {col: monthly_sums[col] for col in monthly_columns}
    sums_row['Lat'] = 'Total'
    sums_row['Lon'] = ''
    sums_row['Annual'] = annual_sum
    
    sums_df = pd.DataFrame([sums_row])
    # Concatenate the original DataFrame with the new sums DataFrame
    proxy_df = pd.concat([proxy_df, sums_df], ignore_index=True)

    # Save the modified proxy data to a new file with the specie name in it
    output_file_name = f"{file_name[:-5]}_{selected_species}.xlsx"
    directory = get_directory_from_filename(file_name)  # Ensure this uses the actual input file name variable
    
    
    
    if directory:
        #save_directory = os.path.normpath(os.path.join(base_directory, directory))
        output_file_path = os.path.join(base_directory, output_file_name)
        
        
    else:
        print("Could not determine the correct directory to save the processed file.")
        return  # Exit the function or handle the error as needed
    
    proxy_df.to_excel(output_file_path, index=False)
    print(f"Data saved to {output_file_name}.\n")
    
    # Display the first five rows and the last row of the saved DataFrame
    print("First 5 rows of the new file:")
    print(proxy_df.head())
    print("\nLast row (totals):")
    print(proxy_df.tail(1))
    print("\nProcessing Further...Please Wait!\n")
    return output_file_path,output_file_name

